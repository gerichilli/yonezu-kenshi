                        <div class="profile__item profile__item--1">
                            <h3 class="heading heading--3">Early musical career</h3>
                            <p class="profile__text">Kenshi Yonezu was born on March 10, 1991, in the rural part of Tokushima, Japan. As a child, Yonezu found it difficult to communicate with other people, especially his father. Yonezu typically felt that his mother understood him.</p>
                            <p class="profile__text">Yonezu's first foray into music was in 2006 during his second year of junior high school, where he formed a band with his friend Hiroshi Nakajima, called Late Rabbit Edda, to play for the school culture festival. He worked as the vocalist, songwriter and occasional guitarist, while Nakajima was the guitarist. In late 2007, he set up a website for the band, posting song lyrics and short novels. Yonezu wrote songs for the band, and between April 2008 and March 2009 uploaded 24 original songs to video sharing website Nico Nico Douga using the name Hachi. None of the songs were widely viewed, with the greatest, "Beelzebub," receiving only 23,000 views. Yonezu created his blog in this period, and called it Tekitō Edda (適当EDDA).</p>
                            <p class="profile__text">Yonezu moved to Osaka after high school, and started attending a fine arts school. While a student there, he started uploading songs using Vocaloid software Hatsune Miku instead, which were increasingly popular. His 2009 song "Musunde Hiraite Rasetsu to Mukuro" (結ンデ開イテ羅刹ト骸) was his first song to receive over 1,000,000 views on the site.[12] Though Yonezu had uploaded over 30 songs sung by himself, he deleted them when his Vocaloid songs became more popular. Yonezu renamed his blog Denshi-chō Hachibangai (電子帖八番, "Electronic Notebook 8th Avenue"), and it was one of five blogs awarded the Diamond Award at the 2009 WebMoney Awards.</p>
                            <p class="profile__text">Late Rabbit Edda remained active until 2010. They were renamed Ernst Eckman, and added a drummer called Sumimoto to their line up. As Ernst Eckman, they released a single song on MySpace, "Oborozuki to Sono Kōan" (オボロヅキとその考案, "Crescent Moon and that Plan").[20] Yonezu began to feel that he did not work well with other people, and decided to work alone on Vocaloid songs exclusively, giving up on being a part of a band.</p>
                        </div>
                        
                        <div class="profile__item profile__item--hide" id="profile-hide">
                            <h3 class="heading heading--3">Major label debut</h3>
                            <p class="profile__text">In March 2011, Yonezu and seven other musicians created Balloom, an independent music label for Internet musicians to widen their musical opportunities. His debut album released in 2012, Diorama, debuted at number 6 and sold over 45,000 copies, becoming the biggest release on the label to date. The album was one of the winners of the 5th CD Shop Awards, an award voted on by music store personnel. Yonezu was picked up as a major label artist under Universal Sigma, and debuted in May 2013 with the single "Santa Maria." He made the shift in order to work with musicians doing similar things as him.</p>
                            <p class="profile__text">On October 28, 2013, Yonezu released his first Vocaloid song in two and a half years, "Donut Hole" (ドーナツホール, Dōnatsu Hōru), using a live band and the vocal Gumi.[27] Kenshi Yonezu released his second album, Yankee, on April 23, 2014,[28] followed by the first concert in his career on June 27.[29] Yonezu's song "Eine Kleine" was written for Tokyo Metro to be used in its 2014 commercial campaign.[30]</p>
                            <p class="profile__text">In 2019, Yonezu wrote the song "Machigaisagashi" for singer Masaki Suda; it won Best Pop Video at the 2019 MTV Video Music Awards Japan. When Billboard Japan published its year-end charts, "Uma to Shika" and "Machigaisagashi" ranked fifth and sixth respectively. Yonezu won the Special Award at the 61st Japan Record Awards held on December 30. "Paprika" won the Grand Prix.</p>
                            <p class="profile__text">Yonezu co-wrote the song "Kite" for Japanese boy band Arashi, which served as the theme song for NHK's coverage of the 2020 Summer Olympics.[citation needed] On August 7, 2020, he performed during Fortnite's Party Royale, singing songs from his latest album Stray Sheep (2020), which had been released two days prior.</p>
